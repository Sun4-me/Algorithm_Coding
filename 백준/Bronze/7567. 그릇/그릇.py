################################################
s = list(input())
################################################
# 첫 바닥은 항상 10센치
# 같은 방향이면 5만 증가
# 반대 방향이면 10 증가
# () 포개지는 형태면 10증가
# )( 반대로 되도 형태도 10 증가
res = 0
while s:
    curr = s.pop()
    if not s:
        res += 10
        break

    if curr == '(' and s[-1] == '(':
        res += 5
    elif curr == ')' and s[-1] == ')':
        res += 5
    else:
        res += 10

print(res)
